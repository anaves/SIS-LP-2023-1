# USO DO COMANDO break
print("---exemplo break---")
for i in range(0,10):
    if i == 6:
        print("***********")
        break
    print(i)
        


# USO do comando continue
print("---exemplo continue---")
for i in range(0,10):
    if i == 6:
        print("***********")
        continue
    print(i)